def random_chinese_name():
    """
    生成随机中文名字
    
    返回:
        str: 随机生成的中文名字
    """
    name_len = random.choice([0, 1, 2, 3])
    if name_len == 0:
        return random_two_name()
    elif name_len == 1:
        return random_three_name()
    elif name_len == 2:
        return random_three_names()
    else:
        return random_four_name()


